const mongoose = require("mongoose");
const tokenSchema = new mongoose.Schema({
  userName: {
    type: String,
    required: true,
  },
  token: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    required: true,
    default: Date.now,
  },
});

module.exports = mongoose.model("token", tokenSchema);